export const mutations = {
    ADD_ITEM(state, data) {
        state.items.push(data)
        state.form = {}
      },
      EDIT_PRODUCT(state, data) {
        let key = data.key
        let item = state.items[key]
        item.name = data.name
        item.price = data.price
      },
      
      DELETE_PRODUCT(state, data) {
          state.items.splice(data, 1)
      },
  
      CHANGE_FORM(state, data) {
          state.form = data;
      }
}